    private void txtPassConfirmKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassConfirmKeyTyped
        if (evt.getKeyChar() == KeyEvent.VK_SPACE || h.CampoRestringuido(evt.getKeyChar())) 
            evt.consume();
        else{
            newP = new String(txtPass.getPassword());
            PConfir = new String(txtPassConfirm.getPassword());
            if (PConfir.equals(newP)) 
            {    txtPassConfirm.setBorder(BorderFactory.createLineBorder(bien, 1)); cont = 1;}
            else { txtPassConfirm.setBorder(BorderFactory.createLineBorder(Color.RED, 1)); cont = 0;}
        }
    }//GEN-LAST:event_txtPassConfirmKeyTyped

