    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        try {
            String old = new String(txtOldPass.getPassword());
            userb.setContraseña(old);
            if (emp.SamePass(userb)) {
                newP = new String(txtPass.getPassword());
                PConfir = new String(txtPassConfirm.getPassword());
                if (PConfir.length()>0 && cont == 1) {
                    userb.setContraseña(newP);
                    emp.ChangePass(userb);
                    JOptionPane.showMessageDialog(null, "Contraseña actualizada !!!");
                    formWindowClosing(null);
                }
                else {JOptionPane.showMessageDialog(null, "No coinciden las contraseñas");}
            }
            else JOptionPane.showMessageDialog(null, "Antigua Contraseña mal ingresada");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAceptarMouseClicked

